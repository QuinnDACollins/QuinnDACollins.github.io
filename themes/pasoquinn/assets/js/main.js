let selected = null

function setShowcaseInfo(source) {
    document.getElementById("showcase-title").innerText = source.title
    document.getElementById("showcase-description").innerText = source.dataset.description
    if (selected != null) {
        selected.classList.remove("showcase-selected")
    }
    selected = source
    selected.classList.add("showcase-selected")
}

window.setShowcaseInfo = setShowcaseInfo